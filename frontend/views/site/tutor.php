<h1 style="text-align: justify;"><a href="#" id="introduction" name="introduction">Введение</a>
</h1>
<p style="text-align: justify;">Опыт преподавания уже солидный, с 2010.
</p>
<p style="text-align: justify;">Преподаю физику, химию, математику, программирование.
</p>
<p style="text-align: justify;">Ученики попадаются разные с разной успеваемостью, разных возрастов (самый младший - пятиклашка, самый старший - мужик 40 лет).
</p>
<h1 style="text-align: justify;"><a name="firstPupil">История про одного из первых учеников</a>
</h1>
<p style="text-align: justify;">Задача передо мной стояла следующая: как-то помочь закончить школу, без двоек. Впоследствии был план поступить туда куда всех берут. Опыта у меня тогда еще вообще почти никакого не было. Стал объяснять как умею. Иногда материал хорошо усваивался, а иногда казалось, что занятие впустую. Конечно, все больше складывалось ощущение, что Максим&nbsp;все больше понимает, рассуждает не по шаблону, но оценки почти не менялись. Правда, двоек тоже не было.
</p>
<p style="text-align: justify;">Так мы и прозанимались год. Дело был в Питере - учился я там в магистратуре. Вернулся в Минск, и примерно через год списались с учеником. &nbsp;Оказалось, что он здоровски по-другому стал относиться к жизни. Вместо того, чтобы поступить туда, куда всех берут, Максим поступил в коледж с it-уклоном, и теперь вполне даже доволен своей работой.
</p>
<h1 style="text-align: justify;"><a id="olympiad" name="olympiad">Одна из последних интересных историй</a>
</h1>
<p style="text-align: justify;">В основном я занимаюсь с ребятами, у которых серьезные проблемы с учебой. Но вот Глеб был другим. Когда мы с ним начали заниматься, он уже был призером международной олимпиады по географии. В-общем, мозги у него хорошо варят. Учился он одновременно недалеко от Лондона и у нас в Минске. Раз в два месяца он прилетал в Минск на две недели, и за это время он должен был закрыть все контрольные, самостоятельные. Сразу я ему объяснял химию, а потом стал целый комплекс.
</p>
<p style="text-align: justify;">Насчет оценок все было супер, но куда интереснее вот что: в конце 11-го класса он умудрился попасть на международную олимпиаду по физике, где все получилось очень даже не супер. Не помню, если честно, то ли бронза, то ли серебро. Конечно, заслуг там моих мало, хотя пару базовых&nbsp;моментов я ему объяснил, которые объясняю всем.
</p>
<h1 style="text-align: justify;"><a id="elseGood" name="elseGood">Еще пару интересных моментов</a>
</h1>
<p style="text-align: justify;">Я сам лично обожаю занятия физической культурой, и редко прохожу мимо турника. Иногда, когда чувствую, что ситуация с математикой стоит на месте, спрашиваю: &quot;Вова, уже у тебя с математикой более-менее. А вот у тебя с подтягиваниями?&quot;. И в ответ часто слышу без особой радости: &quot;один-ноль&quot;. Но через год, когда заново задаю этот вопрос, уже слышу радостный голос: &quot;8-12&quot;. И как же часто случается так, что у ученика начинает сдвиг начинает происходить как в умственной так и в физической сфере одновременно. Вот именно эти результаты меня больше всего восхищают.
</p>
<h1 style="text-align: justify;"><a id="method" name="method">Как же я преподаю</a>
</h1>
<p style="text-align: justify;">На данный момент я понимаю, что самый надежный способ сделать хорошие оценки, это прорешивать задания в сборниках для самостоятельных и контрольных работ. Но, ни разу такой подход не дал интересных результатов. Оценки хорошие - и все. Ну вот не чувствую я, что ученик сам рассуждает.&nbsp;
</p>
<p style="text-align: justify;">Я закончил университет, со мной учились ребята, которые на выходе имели более высокий средний балл. Но сегодня они ходят на работу и пыхтят на жизнь, они в принципе боятся что-то менять. Они исполнительны, но не вижу блеска в глазах. Конечно, университетская среда и среда колледжа весьма различаются. И я не против, что в выпускных классах надо работать в большей степени на оценку. Но если этим начинать заниматься в классе 7-8 (прорешивать задания из сборника), можно так и не научиться думать самостоятельно.
</p>
<p style="text-align: justify;">Когда я преподаю, я пытаюсь сделать так, чтобы ученик сам дошел до ответа на вопрос. Я могу долго мусолить одну и ту же задачу, понимая, что 4 задачи так и&nbsp;останутся не решенными.
</p>
<p style="text-align: justify;">Да, согласен, есть проблемы с преподавательским составомв Беларуси. Но в то же время все больше хороших онлайн материалов появляется в сети. В моем понимании репетитор должен подталкивать и не допускать захода в полный тупик.
</p>
<p style="text-align: justify;">Все, что я объясняю, я объясняю через базу, и требую того же от учеников. Если слышу набор умных слов, сразу говорю: &quot;хватит умничать. представь что я второклашка. объясни мне по-простому)&quot;
</p>
<p style="text-align: justify;">Именно база логических наук позволила мне легко стать программистом, не заканчивая для этой цели специальный университет (я химик по образованию).
</p>
<h1 style="text-align: justify;"><a id="wish" name="wish">Про то, когда хотелось бы лучше</a>
</h1>
<p style="text-align: justify;">Есть у меня и не самый лучший опыт преподавания. В основном, история примерно следующая. Ученик каждый день посещает спортивные тренировки, прихожу к нему вечером домой, а он домашку почти и не пытался делать. В-общем, если ко мне попасть с изначально плохими оценками и не делать то, что я задаю, вообще, тогда обычно все плохо заканчивается.
</p>
<h1 style="text-align: justify;"><a id="help" name="help">Могу просто так помочь</a>
</h1>
<p style="text-align: justify;">Я работаю и за работу беру деньги. Но я могу помочь на безвозмездной основе. Я посещаю много обществееных мест, где можно о чем угодно переговорить. Пожалуйста, на спикин клабах, волейболе, в велопоходах, на танцах спрашивайте меня смело о чем угодно. Я серьезно хочу, чтобы вокруг меня было как можно большее количество образованных людей. &nbsp;
</p>
<h1 style="text-align: justify;"><a id="conclusion" name="conclusion">Заключение</a>
</h1>
<p style="text-align: justify;">В-общем, со мной не будет так, что я пришел и всему научил. Подтолкнуть, разобраться, объяснить сложное на пальцах - смогу. Но вы мне должны помочь. Можно не делать все то, что я задаю на дом. Но пытаться делать обязательно. Задавайте мне вопросы в социальных сетях, звоните, пишите. Но главная моя цель: спровоцировать вас думать самостоятельно. Я согласен, что физика пригодится не каждому в жизни. Но вот умение спокойно сложную проблему разбивать на маленькие несложные пригодится и поможет каждому.
</p>
